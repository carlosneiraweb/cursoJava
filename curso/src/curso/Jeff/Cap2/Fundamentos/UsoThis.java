public class UsoThis {
    
    private int x, y;
    private int width, height;
        
    public UsoThis() {
        this(0, 0, 1, 1);
    }
    public UsoThis(int width, int height) {
        this(0, 0, width, height);
    }
    public UsoThis(int x, int y, int width, int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
    
    
    
    //fin usoThis
    
}
